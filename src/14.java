public class SimpleSchoolProject {
    public static void main(String[] args) {
        // Example of how to initialize variables in Java
        int x = 5;
        String name = "Alice";
        
        // Example of using if-else statements
        if (x < 10) {
            System.out.println("Value of x is less than 10");
        } else {
            System.out.println("Value of x is not less than 10");
        }
        
        // Example of using loops
        for (int i = 0; i < 5; i++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }
    }
}
